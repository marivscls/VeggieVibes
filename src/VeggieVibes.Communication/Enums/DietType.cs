namespace VeggieVibes.Communication.Enums
{
    public enum DietType
    {
        Vegetarian = 1,            // Dieta vegetariana geral
        Vegan = 2,                 // Sem produtos de origem animal
        OvoVegetarian = 3,         // Inclui ovos
        LactoVegetarian = 4,       // Inclui laticínios
        OvoLactoVegetarian = 5,    // Inclui ovos e laticínios
        Pescatarian = 6,           // Inclui peixe, mas sem carne de outros animais
        GlutenFree = 7,            // Sem glúten
        LowCarb = 8,               // Baixo em carboidratos
        HighProtein = 9,           // Alto teor de proteína
        RawVegan = 10,             // Apenas alimentos crus e sem origem animal
        WholeFoodPlantBased = 11   // Baseada em alimentos integrais e vegetais
    }
}
